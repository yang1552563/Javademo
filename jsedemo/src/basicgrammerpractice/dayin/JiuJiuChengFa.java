package basicgrammerpractice.dayin;

public class JiuJiuChengFa {
    public static void main(String[] args) {
        for (int x = 0; x<10;x++){
            for (int b = 1; b<=x;b++)
            {
                System.out.print(x+"*"+b+"="+(x*b)+" ");
            }
            System.out.println();
        }
    }
}
